    private void CSVPathBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CSVPathBtnMouseClicked
        boolean returned = false;
        while (returned == false) {
            JFileChooser csvFileChooser = new JFileChooser();
            csvFileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            int csvFileRetVal = csvFileChooser.showOpenDialog(jLabel1);
            if (csvFileRetVal == JFileChooser.CANCEL_OPTION) {
                return;
            }
            File csvFile = csvFileChooser.getSelectedFile();
            if (csvFile.isDirectory()) {
                String csvName = JOptionPane.showInputDialog("Input CSV File Name");
                if (!csvName.contains(".csv")) {
                    csvName = csvName + ".csv";
                }
                csvFilePath = csvFile.getAbsolutePath() + "/" + csvName;
                System.err.println("" + csvFilePath);
                returned = true;
            } else {
                JOptionPane.showMessageDialog(csvFileChooser, "You selected a folder not a file");
            }
        }
    }//GEN-LAST:event_CSVPathBtnMouseClicked

